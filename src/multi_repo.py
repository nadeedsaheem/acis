def resolve_qualified_scope(class_owner_raw, ns_owner_raw, global_classes_and_structs, global_namespaces):
    if not class_owner_raw:
        return None, ns_owner_raw, False

    parts = [p.strip() for p in class_owner_raw.split("::") if p.strip()]
    if not parts:
        return None, ns_owner_raw, False

    # Find if any part is a known class/struct
    class_idx = -1
    for i, part in enumerate(parts):
        if part in global_classes_and_structs:
            class_idx = i
            break

    # Fallback: check if the last part is not a namespace
    if class_idx == -1:
        if parts[-1] in global_namespaces:
            class_idx = -1
        else:
            # Assume last part is class if it's not in namespaces
            class_idx = len(parts) - 1

    if class_idx != -1:
        class_parts = parts[class_idx:]
        ns_parts = parts[:class_idx]
        
        resolved_class = "::".join(class_parts)
        
        # Merge namespaces
        all_ns = []
        if ns_owner_raw:
            all_ns.extend([p.strip() for p in ns_owner_raw.split("::") if p.strip()])
        all_ns.extend(ns_parts)
        resolved_ns = "::".join(all_ns) if all_ns else None
        
        return resolved_class, resolved_ns, True
    else:
        # All parts are namespaces
        all_ns = []
        if ns_owner_raw:
            all_ns.extend([p.strip() for p in ns_owner_raw.split("::") if p.strip()])
        all_ns.extend(parts)
        resolved_ns = "::".join(all_ns) if all_ns else None
        
        return None, resolved_ns, False

